#' @rdname Helper-functions
#' @keywords internal
#' @export

tclvalue_chr <- function(x, trim = TRUE, ...) {
    rez <- as.character(tclvalue(x))
    if (isTRUE(trim)) {rez <- trimws(rez, ...)}
    unname(rez)
}

#' @rdname Helper-functions
#' @keywords internal
#' @export
tclvalue_chr_split <- function(x) {
    sapply(unlist(strsplit(tclvalue(x), " ")), as.character)
    unname(x)
}
