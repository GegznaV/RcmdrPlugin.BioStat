#' command_new_dataset
#'
#' Code of this function is taken from `Rcmdr` package and slightly modified.
#'
#' @export
#' @keywords internal
window_dataset_rename <- function() {
    # ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
    # Window to choose dataset's name

    initializeDialog(title = gettext_bs("Rename the active dataset"))
    # Title ------------------------------------------------------------------
    fg_col <- Rcmdr::getRcmdr("title.color")
    tkgrid(label_rcmdr(
        top,
        text = gettext_bs("Rename the dataset"),
        font = tkfont.create(weight = "bold", size = 9),
        fg = fg_col),
        pady = c(5, 9), columnspan = 2)
    # ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
    dsname <- tclVar(unique_df_name(str_glue(
        "{activeDataSet()}_renamed")))
    entryDsname <- ttkentry(top, width = "40", textvariable = dsname)

    # ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
    onOK <- function() {
        dsnameValue <- trim.blanks(tclvalue(dsname))
        # ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
        closeDialog()
        # Checks if no name is entered
        if (dsnameValue == "") {
            errorCondition(
                recall = window_dataset_rename,
                message = gettext_bs("You must enter the name of the dataset."))
            return()
        }

        # Check validity of the entered name
        if (!is.valid.name(dsnameValue)) {
            errorCondition(
                recall = window_dataset_rename,
                message = str_glue('"{dsnameValue}" ',
                                   gettext_bs("is not a valid name for a dataset."))
            )
            return()
        }

        # Check if a dataset with the same name exists in the workspace
        if (is.element(dsnameValue, listDataSets())) {
            if ("no" == tclvalue(checkReplace(dsnameValue, gettext_bs("Dataset")))) {
                window_dataset_rename()
                return()
            }
        }
        # ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
        # Edit window
        ds_to_rename <- activeDataSet()

        # Two commands are used to avoid error message in R Commander
        command1 <- str_glue(
            "# Rename the dataset \n",
            "{dsnameValue} <- {ds_to_rename}")
        justDoIt(command1)
        activeDataSet(dsnameValue)

        command2 <- str_glue("remove({ds_to_rename})")
        justDoIt(command2)

        # ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
        logger(paste(command1, command2, sep = "\n"))
        tkfocus(CommanderWindow())
    }
    # ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

    OKCancelHelp()

    tkgrid(labelRcmdr(top,
                      fg = getRcmdr("title.color"),
                      text = gettext_bs("Dataset to rename: ")),
           labelRcmdr(top,
                      text = activeDataSet()),

           sticky = "w", pady = c(5, 10))

    tkgrid(labelRcmdr(top,
                      fg = getRcmdr("title.color"),
                      text = gettext_bs("Enter a new name:  ")),
           entryDsname,
           sticky = "e", pady = c(0, 10))


    tkgrid(buttonsFrame, columnspan = "2", sticky = "ew")
    tkgrid.configure(entryDsname, sticky = "w")
    dialogSuffix(focus = entryDsname)
}
